type ExpValue = {
  level: number;
  type:
    | "EXP"
    | "EXTREME_MONPARK"
    | "ANGLER_COMPANY"
    | "HIGH_MOUNTAIN"
    | "EXP_VOUCHERS"
    | "ADVANCED_EXP_VOUCHERS"
    | "VIP_REST"
    | "NIGHTMARE";
};

export const getExpValue = ({ level, type }: ExpValue) => {
  switch (type) {
    case "EXP":
      if (level < 1 || level > 299) return 0;
      return LEVEL_EXP_TABLE[level as keyof typeof LEVEL_EXP_TABLE];
    case "EXTREME_MONPARK":
      if (level < 260 || level > 299) return 0;
      return EXTREME_MONPARK_EXP_TABLE[level as keyof typeof EXTREME_MONPARK_EXP_TABLE];
    case "ANGLER_COMPANY":
      if (level < 270 || level > 299) return 0;
      return ANGLER_COMPANY_EXP_TABLE[level as keyof typeof ANGLER_COMPANY_EXP_TABLE];
    case "HIGH_MOUNTAIN":
      if (level < 260 || level > 299) return 0;
      return HIGH_MOUNTAIN_EXP_TABLE[level as keyof typeof HIGH_MOUNTAIN_EXP_TABLE];
    case "EXP_VOUCHERS":
      if (level < 200) return 0;
      if (level > 260) return EXP_VOUCHERS_TABLE[260];
      return EXP_VOUCHERS_TABLE[level as keyof typeof EXP_VOUCHERS_TABLE];
    case "ADVANCED_EXP_VOUCHERS":
      if (level < 260 || level > 299) return 0;
      return ADVANCED_EXP_VOUCHERS_TABLE[level as keyof typeof ADVANCED_EXP_VOUCHERS_TABLE];
    case "VIP_REST":
      if (level < 101 || level > 299) return 0;
      if (level > 289) return VIP_REST_TABLE[289];
      return VIP_REST_TABLE[level as keyof typeof VIP_REST_TABLE];
    case "NIGHTMARE":
      if (level < 280 || level > 299) return 0;
      return NIGHTMARE_EXP_TABLE[level as keyof typeof NIGHTMARE_EXP_TABLE];
    default:
      return 0;
  }
};
